#include "../../include/libft.h"

char	*ft_strchr(const char *s, int c)
{
	int				i;
	unsigned char	chr;

	i = 0;
	chr = (unsigned char)c;
	while (s[i] != '\0' && s[i] != chr)
		s++;
	if (s[i] == chr)
		return ((char *)&s[i]);
	return (NULL);
}
